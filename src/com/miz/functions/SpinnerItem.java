package com.miz.functions;

public class SpinnerItem {
	
	private String title, subtitle;
	
	public SpinnerItem(String title, String subtitle) {
		this.title = title;
		this.subtitle = subtitle;
	}
	
	public String getTitle() {
		return title;
	}
	
	public String getSubtitle() {
		return subtitle;
	}
	
}