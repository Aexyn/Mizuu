package com.miz.functions;

public class Season {

	private String mCoverPath;
	private int mSeason;
	
	public void setCoverPath(String cover) {
		mCoverPath = cover;
	}
	
	public String getCoverPath() {
		return mCoverPath;
	}
	
	public void setSeason(int season) {
		mSeason = season;
	}
	
	public int getSeason() {
		return mSeason;
	}
}
